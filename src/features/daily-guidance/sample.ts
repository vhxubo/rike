export const SAMPLE_DAILY_GUIDANCE: DailyGuidance[] = DAILY_GUIDANCE_DATA.map(
  ([date, text, category]) => ({
    date,
    id: `guidance-${date}`,
    text,
    category,
    tags: ['每日赠语', '学习'],
  }),
)

export function getDailyGuidance(date: string) {
  return SAMPLE_DAILY_GUIDANCE.find((guidance) => guidance.date === date) ??
    (date < SAMPLE_DAILY_GUIDANCE[0].date
      ? SAMPLE_DAILY_GUIDANCE[0]
      : SAMPLE_DAILY_GUIDANCE.at(-1)!)
}
